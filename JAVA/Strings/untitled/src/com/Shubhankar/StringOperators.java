package com.Shubhankar;

import java.util.ArrayList;
import java.util.Arrays;

public class StringOperators {
    public static void main(String[] args) {
        float a = 2785.365486f;
        System.out.printf("Printed number is %.2f",a);
        System.out.println();
        System.out.printf("PIE : %.3f",Math.PI);
        System.out.println();
        System.out.printf("Hello my name is %s and i am %s","Tony","cool");
        System.out.println();
        System.out.println('a'+'b');
        System.out.println((char)('a'+3));
        System.out.println('A'*'a');
        System.out.println("A"+new ArrayList<>());
        System.out.println(new Integer(655));
    }
}
